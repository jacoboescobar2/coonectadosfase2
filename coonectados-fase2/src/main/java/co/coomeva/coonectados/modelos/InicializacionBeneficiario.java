package co.coomeva.coonectados.modelos;

public class InicializacionBeneficiario {

	private String nombreBeneficiario;
	private String parentesco;
	private String planActual;
	private String cuotaActual;
	private String planNovedad;
	private String coutoNovedad;
	private String mensaje;
	
	
	public String getNombreBeneficiario() {
		return nombreBeneficiario;
	}
	public void setNombreBeneficiario(String nombreBeneficiario) {
		this.nombreBeneficiario = nombreBeneficiario;
	}
	public String getParentesco() {
		return parentesco;
	}
	public void setParentesco(String parentesco) {
		this.parentesco = parentesco;
	}
	public String getPlanActual() {
		return planActual;
	}
	public void setPlanActual(String planActual) {
		this.planActual = planActual;
	}
	public String getCuotaActual() {
		return cuotaActual;
	}
	public void setCuotaActual(String cuotaActual) {
		this.cuotaActual = cuotaActual;
	}
	public String getPlanNovedad() {
		return planNovedad;
	}
	public void setPlanNovedad(String planNovedad) {
		this.planNovedad = planNovedad;
	}
	public String getCoutoNovedad() {
		return coutoNovedad;
	}
	public void setCoutoNovedad(String coutoNovedad) {
		this.coutoNovedad = coutoNovedad;
	}
	public String getMensaje() {
		return mensaje;
	}
	public void setMensaje(String mensaje) {
		this.mensaje = mensaje;
	}
	
	
}
